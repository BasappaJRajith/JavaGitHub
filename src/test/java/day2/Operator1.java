package day2;

public class Operator1 {
    public static void main(String[] args) {


        int a = 20;
        int b = 10;

        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        float div = a / b;

        System.out.println("sum= " + sum);
        System.out.println("sub= " + sub);
        System.out.println("mul =" + mul);
        System.out.println("div =" + div);
    }
}
